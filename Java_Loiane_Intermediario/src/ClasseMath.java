
public class ClasseMath {

	public static void main(String[] args) {
		System.out.println(Math.pow(2, 3));//dois elevado 3 (2*2*2=8)
		System.out.println(Math.round(4.7));//Aredondando valor para cima
		System.out.println(Math.round(4.4));//aredondando valor para baixo
		System.out.println(Math.ceil(4.4));//arredonda valor para cima
		System.out.println(Math.ceil(4.7));//arredonda valor para cima
		System.out.println(Math.floor(4.4));//aredonda para baixo
		System.out.println(Math.floor(4.7));//aredonda para baixo
		System.out.println(Math.round(Math.random()*100));//gerar numero aleatório de 0 até 100
		System.out.println(Math.sin(Math.toRadians(30)));//função trigunimétrica (SENO)
		System.out.println(Math.cos(Math.toRadians(1))); //(COSSENO)
		System.out.println(Math.tan(Math.toRadians(45)));//(TANGENTE)

	}

}
